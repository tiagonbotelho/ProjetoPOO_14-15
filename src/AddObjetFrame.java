    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String forma;
        String cor;
        String tipo;
        Objeto objet;
        int x,y;
        if(!verificaForma(jTextField2.getText())){
            jLabel6.setText("Forma inválida");
            return;
        }
        else{
            forma = jTextField2.getText();
        }
        if(!verificaCor(jTextField1.getText())){
            jLabel6.setText("Cor inválida");
            return;
        }
        else{
            cor = jTextField1.getText();
        }
        if(!verificaTipo(jTextField4.getText())){
            jLabel6.setText("Tipo inválida");
            return;
        }
        else{
            tipo = jTextField4.getText();
        }        
        try{
            if(!jTextField5.getText().equals("") && Integer.parseInt(jTextField5.getText())>0 && Integer.parseInt(jTextField5.getText())<=a.getWidth()){
                x = Integer.parseInt(jTextField5.getText());
            }
            else{
                jLabel6.setText("X tem de estar entre os limites do ambiente. (0,"+a.getWidth()+")");
                return;
            }
        }catch(NumberFormatException e){
            jLabel6.setText("X inválido");
            return;
        }        
        try{
            if(!jTextField3.getText().equals("") && Integer.parseInt(jTextField3.getText())>0 && Integer.parseInt(jTextField3.getText())<=a.getWidth()){
                y = Integer.parseInt(jTextField3.getText());
            }
            else{
                jLabel6.setText("Y tem de estar entre os limites do ambiente. (0,"+a.getHeight()+")");
                return;
            }
        }catch(NumberFormatException e){
            jLabel6.setText("Y inválido");
            return;
        }
        objet = new Objeto(forma,cor,new Coord(x,y),tipo);
        if(a.addEntidade(objet)){
            jLabel6.setText("Objeto adicionado.");
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField5.setText("");
        }
        else{
            jLabel6.setText("Posição já ocupada");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

