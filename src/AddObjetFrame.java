    private boolean verificaForma(String forma){
        for(String aux:Entidade.formas){
            if(forma.equals(aux)){
                return true;
            }
        }
        return false;
    }
    private boolean verificaTipo(String tipo){
        for(String aux:Objeto.tipos){
            if(tipo.equals(aux)){
                return true;
            }
        }
        return false;
    }
    private boolean verificaCor(String cor){
        for(String aux:Entidade.cores){
            if(cor.equals(aux)){
                return true;
            }
        }
        return false;
    }    
